  <font size="3pt" color="red"  >ATTENDANCE</font><br/>
1. All students shall maintain a minimum attendance of 85% in each course
registered. In case of shortfall in this, the Principal of the College shall
consider and may condone deficiency up to a limit of 10% in special cases
after due recommendation by the concerned Head of the Department.<br/>
2.Any student failing to meet the above standard of attendance in any
course(s) registered, shall not be allowed to appear for SEE of such
course(s).<br/>
 3.Attendance at CIE and SEE: Attendance at all examinations, both CIE and
SEE of each course registered shall be compulsory for the students and
there shall not be any provision for re-examinations/consideration.<br/>
 4.Any student against whom any disciplinary action by the College is
pending shall not be permitted to attend any SEE in that Semester.
<br/>
 5.Each Semester is considered as a unit and the candidate has to put in a
minimum attendance of 85% in each subject with a provision of
condonation of 10% attendance by the Principal for reasons such as
medical emergencies, participation in sport, cultural activities, seminars,
workshops and paper presentation etc at the level of University, State, and
National.<br/>
6.The basis for the calculation of the attendance shall be the period
prescribed by the College by its calendar of events. For the first semester
students, the same is reckoned from the date of admission to the course.<br/>
7.The students shall be informed about their attendance position
periodically by the College so that the students can strive to make up the
shortage. However, non-receipt of such information from the college will
not be considered as valid reason for exemption from the attendance
requirements.<br/>
8.If a student does not fulfill the attendance requirements in any subject
he/she is not permitted to attend the Semester End Evaluation in that
subject and is deemed to have been awarded F grade in that subject.<br/>
 9.In respect of Integrated Courses 85% of attendance shall be maintained in
Theory as well as practical component of the course. Failing to maintain
the 85% attendance in anyone component the student will not be
permitted to take up SEE in the course.

